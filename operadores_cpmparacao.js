let valor1 = 20;
let valor2 = "30";
// igual a
console.log(valor1 == valor2); // false

// estritamente igual a 
console.log(valor1 === valor2); // true

// diferente de
console.log(valor1 != valor2); // false

// estritamente diferente de
console.log(valor1 !== valor2); // true
// maior que
console.log(valor1 > valor2); // false

// maior ou igual a
console.log(valor1 >= valor2); // true

// menor que
console.log(valor1 < valor2); // false



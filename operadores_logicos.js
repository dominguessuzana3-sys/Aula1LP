// operradores lógicos && 
const fretegratis = false
const estoque = true;

console.log (fretegratis && estoque); // false


// operradores lógicos ||

console.log (fretegratis || estoque); // true

// operradores lógicos ! (negação)
console.log (!fretegratis);


// dupla negação !!

const nome = "Suzana";
console.log(!!nome); // true

const nomeVazio = "";
console.log(!!nomeVazio); // false

// operador lógico com valores não booleanos
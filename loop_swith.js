// switch (casos)

const codigoPromocao = 50;

switch (codigoPromocao) {
    case 100:
        console.log ("Produto tenis.");
        break;
        case 200:
        console.log ("Produto camiseta.");
        break;
        case 510:
        console.log ("Produto mochila.");
        break;
    default:
        console.log ("Produto não encontrado.");
        break;
}


const diasSemana = 1;

switch (diasSemana) {
    case 1:
        console.log ("Domingo");
        break;
    case 2:
        console.log ("Segunda-feira");
        break;
    case 3:
        console.log ("Terça-feira");
        break;
    case 4:
        console.log ("Quarta-feira");
        break;
    case 5:
        console.log ("Quinta-feira");
        break;
    case 6:
        console.log ("Sexta-feira");
        break;
    case 7:
        console.log ("Sábado");
        break;
    default:
        console.log ("Dia inválido.");
        break;
}
